// Program to calculate volume of given figures

public class CalculateVolume
{
public static void main(String[ ] args)
{

//Declare variable to hold length
int length;

//assign value to length
length = 4;

//Declare variable to hold width
int width;

//assign value to width
width = 2;

//Declare variable to hold depth
int depth;

//assign value to depth
depth = 12;

//Declare variable to hold volume
int volume;

//assign value to volume
volume = 0;
 

//perform cal

volume= length * width * depth;

//display volume on screen

System.out.print("The volume is ");
System.out.print(volume);
System.out.print(" inches");
}
}


